if data == 0:
    print('Domingo')
elif data == 1:
    print('Segunda-feira')
elif data == 2:
    print('Terça-feira')
elif data == 3:
    print('Quarta-feira')
elif data == 4:
    print('Quinta-feira')
elif data == 5:
    print('Sexta=feira')
elif data == 6:
    print('Sabado')
